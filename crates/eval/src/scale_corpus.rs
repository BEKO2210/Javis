/// Build a deterministic corpus of `target_sentences` sentences using
/// `seed` as the only source of randomness. Returns the sentence list,
/// the unique content-word vocabulary, the list of words suitable as
/// query keys (those that appear in at least 2 sentences), and a
/// per-word ground-truth map of `query → sentence indices that
/// contain it`.
pub struct ScaleCorpus {
    pub sentences: Vec<String>,
    pub vocabulary: Vec<String>,
    pub queries: Vec<String>,
    pub ground_truth: BTreeMap<String, Vec<usize>>,
}

pub fn build_scale_corpus(target_sentences: usize, seed: u64) -> ScaleCorpus {
    let mut rng = Rng::new(seed);
    let mut sentences: Vec<String> = Vec::with_capacity(target_sentences);
    let mut ground_truth: BTreeMap<String, Vec<usize>> = BTreeMap::new();

    for sidx in 0..target_sentences {
        // Round-robin through domains so vocabulary grows uniformly.
        let domain = &DOMAINS[sidx % DOMAINS.len()];
        let template_idx = (rng.next_u64() as usize) % domain.templates.len();
        let template = domain.templates[template_idx];

        // Draw without replacement from the domain pools so the same
        // word does not appear twice in one sentence.
        let s1 = pick(domain.subjects, &mut rng);
        let mut s2 = pick(domain.subjects, &mut rng);
        if s2 == s1 && domain.subjects.len() > 1 {
            s2 = pick_excluding(domain.subjects, &mut rng, &[s1]);
        }
        let a1 = pick(domain.attributes, &mut rng);
        let c1 = if domain.contexts.is_empty() {
            ""
        } else {
            pick(domain.contexts, &mut rng)
        };

        let mut sentence = template.to_string();
        sentence = sentence.replace("{S2}", s2);
        sentence = sentence.replace("{S}", s1);
        sentence = sentence.replace("{A}", a1);
        sentence = sentence.replace("{C}", c1);

        // Record ground-truth: every distinct content word in the
        // sentence maps back to this index.
        let words = content_words(&sentence);
        for w in &words {
            ground_truth.entry(w.clone()).or_default().push(sidx);
        }
        sentences.push(sentence);
    }

    // Vocabulary is just the union of every content word ever
    // recorded.
    let vocabulary: Vec<String> = ground_truth.keys().cloned().collect();

    // Queries: every word that appears in at least two sentences.
    // Single-occurrence words are too sparse to give meaningful
    // precision / recall stats — we'd just be measuring whether the
    // exact training pattern is recallable, not whether the
    // architecture generalises. Words appearing 2+ times are the
    // realistic "what does Javis do for a query that has multiple
    // candidate answers" case.
    let queries: Vec<String> = ground_truth
        .iter()
        .filter(|(_, idxs)| idxs.len() >= 2)
        .map(|(w, _)| w.clone())
        .collect();

    ScaleCorpus {
        sentences,
        vocabulary,
        queries,
        ground_truth,
    }
}

fn pick<'a>(pool: &'a [&'static str], rng: &mut Rng) -> &'a str {
    pool[(rng.next_u64() as usize) % pool.len()]
}

fn pick_excluding<'a>(pool: &'a [&'static str], rng: &mut Rng, exclude: &[&str]) -> &'a str {
    for _ in 0..16 {
        let candidate = pool[(rng.next_u64() as usize) % pool.len()];
        if !exclude.contains(&candidate) {
            return candidate;
        }
    }
    pool[0]
}

/// Lower-case content words (alphabetic tokens of length ≥ 3) with
/// English stop-words removed. Same filter the `viz` `AppState` uses.
fn content_words(sentence: &str) -> Vec<String> {
    const STOP: &[&str] = &[
        "the", "and", "for", "are", "with", "from", "that", "this", "into", "than", "have", "has",
        "had", "was", "were", "been", "being", "its", "during", "across", "after", "before",
        "many", "most", "long", "form", "form.", "they", "their", "more", "less", "such",
    ];
    let stops: HashSet<&str> = STOP.iter().copied().collect();
    let mut out = BTreeSet::new();
    for raw in sentence.split_whitespace() {
        let clean: String = raw
            .chars()
            .filter(|c| c.is_alphabetic() || *c == '-')
            .collect::<String>()
            .to_lowercase();
        if clean.len() < 3 {
            continue;
        }
        if stops.contains(clean.as_str()) {
            continue;
        }
        out.insert(clean);
    }
    out.into_iter().collect()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn corpus_is_deterministic_from_seed() {
        let a = build_scale_corpus(50, 42);
        let b = build_scale_corpus(50, 42);
        assert_eq!(a.sentences, b.sentences);
        assert_eq!(a.queries, b.queries);
        assert_eq!(a.ground_truth, b.ground_truth);
    }

    #[test]
    fn corpus_grows_vocabulary_with_size() {
        let small = build_scale_corpus(20, 1);
        let large = build_scale_corpus(200, 1);
        assert!(
            large.vocabulary.len() > small.vocabulary.len(),
            "larger corpus should contain more unique content words ({} vs {})",
            small.vocabulary.len(),
            large.vocabulary.len(),
        );
    }

    #[test]
    fn ground_truth_indexes_are_consistent() {
        let c = build_scale_corpus(100, 7);
        for (word, idxs) in &c.ground_truth {
            for &idx in idxs {
                let s = &c.sentences[idx];
                assert!(
                    content_words(s).contains(word),
                    "ground truth says sentence {idx} contains '{word}', but it does not: {s}",
                );
            }
        }
    }

    #[test]
    fn queries_appear_at_least_twice() {
        let c = build_scale_corpus(200, 11);
        for q in &c.queries {
            let occurrences = c.ground_truth.get(q).map(|v| v.len()).unwrap_or(0);
            assert!(
                occurrences >= 2,
                "query '{q}' should appear in ≥ 2 sentences, has {occurrences}",
            );
        }
    }
}
