//! Deterministic scale-corpus generator for the validation-at-scale
//! benchmark.
//!
//! The five-paragraph `wiki_corpus` is fine for unit tests — it's not
//! credible as a publication artefact for "Javis works at scale". The
//! corpus produced here is template-driven, fully reproducible from a
//! seed, and parameterisable in vocabulary size and sentence count
//! independently.
//!
//! Each generated sentence belongs to one of eight knowledge domains
//! (chemistry, biology, geography, history, technology, music, sports,
//! food). Content words are pulled from a curated vocabulary list per
//! domain; templates string two-to-four content words into a
//! plausible factoid. Ground truth is recorded by construction: for
//! every content word `w` we know the exact set of sentence indices
//! that contain it, which lets the benchmark compute precision /
//! recall / false-positive rates against a real reference instead of
//! "well, the trained word came back".
//!
//! ## Reproducibility
//!
//! A single `u64` seed plus a sentence count fully determines the
//! output. A reviewer can re-run the benchmark with the same numbers
//! by passing the same seed; there is no external dataset and no
//! network dependency, only this file and the `Rng` type from
//! `snn-core`.

